import os
import time
from typing import Dict, List, Any
import statistics

from shorts_generator.pipeline import generate_shorts
from shorts_generator.logger import get_logger

logger = get_logger("benchmark")

# Default test suite mapping categories to stable, relatively short YouTube videos
TEST_SUITE = {
    "Podcast": "https://www.youtube.com/watch?v=kGgEDsVw8yY",      # Short podcast clip
    "Interview": "https://www.youtube.com/watch?v=M2_NMCAEzM4",    # Short interview clip
    "Tutorial": "https://www.youtube.com/watch?v=kqtD5dpn9C8",     # Python in 100 seconds
    "Finance": "https://www.youtube.com/watch?v=p7WKZq3wXmA",      # Short finance explanation
    "Gaming": "https://www.youtube.com/watch?v=jNQXAC9IVRw",       # Me at the zoo (historic/gaming stand-in)
    "Comedy": "https://www.youtube.com/watch?v=8V3-H7nK3bY",       # Short comedy bit
    "Lecture": "https://www.youtube.com/watch?v=Q74pD4_Y9O8",      # Short TED/lecture clip
}


def run_benchmark():
    logger.info("Starting Automated Benchmark Suite...")
    
    results: Dict[str, Dict[str, Any]] = {}
    
    total_start = time.perf_counter()
    
    for category, url in TEST_SUITE.items():
        logger.info(f"\n{'='*50}\nTesting Category: {category}\nURL: {url}\n{'='*50}")
        
        cat_start = time.perf_counter()
        
        try:
            # We request 1 clip per category to keep the benchmark fast
            result = generate_shorts(
                youtube_url=url,
                num_clips=1,
                aspect_ratio="9:16",
                mode="local",
                profile="youtube"
            )
            
            cat_time = time.perf_counter() - cat_start
            
            # Aggregate reports
            valid_clips = [c for c in result.shorts if not c.error and c.quality_report]
            
            if valid_clips:
                avg_subtitles = statistics.mean([c.quality_report["subtitle_quality"] for c in valid_clips])
                avg_coherence = statistics.mean([c.quality_report["hook_quality"] for c in valid_clips])
                avg_crop = statistics.mean([c.quality_report["crop_stability"] for c in valid_clips])
                
                # Render/Export Quality is an aggregate of editing smoothness and audio quality
                avg_render = statistics.mean([(c.quality_report["editing_smoothness"] + c.quality_report["audio_quality"])/2 for c in valid_clips])
                
                results[category] = {
                    "status": "Success",
                    "time": cat_time,
                    "render_quality": avg_render,
                    "subtitle_quality": avg_subtitles,
                    "clip_coherence": avg_coherence,
                    "crop_stability": avg_crop,
                    "export_quality": avg_render # Same as render quality for now
                }
            else:
                results[category] = {"status": "Failed (No valid clips)", "time": cat_time}
                
        except Exception as e:
            logger.error(f"Category {category} failed: {e}")
            results[category] = {"status": f"Failed ({e})", "time": time.perf_counter() - cat_start}
            
    total_time = time.perf_counter() - total_start
    
    # Generate Markdown Report
    report_path = "benchmark_report.md"
    logger.info(f"\nGenerating {report_path}...")
    
    with open(report_path, "w", encoding="utf-8") as f:
        f.write("# 🧪 AI-Youtube-Shorts-Generator Benchmark Report\n\n")
        f.write(f"**Total Suite Execution Time:** {total_time:.2f} seconds\n\n")
        
        f.write("## Category Performance Matrix\n\n")
        f.write("| Category | Status | Processing Time | Rendering Quality | Subtitle Quality | Clip Coherence | Crop Stability | Export Quality |\n")
        f.write("|---|---|---|---|---|---|---|---|\n")
        
        for category, data in results.items():
            if data["status"] == "Success":
                f.write(f"| **{category}** | ✅ Success | {data['time']:.2f}s | {data['render_quality']:.1f}/100 | {data['subtitle_quality']:.1f}/100 | {data['clip_coherence']:.1f}/100 | {data['crop_stability']:.1f}/100 | {data['export_quality']:.1f}/100 |\n")
            else:
                f.write(f"| **{category}** | ❌ {data['status']} | {data['time']:.2f}s | N/A | N/A | N/A | N/A | N/A |\n")
                
        f.write("\n\n> *Note: Metrics are automatically generated by the internal Quality Analyzer module.*")
        
    logger.info("Benchmark complete!")

if __name__ == "__main__":
    run_benchmark()
